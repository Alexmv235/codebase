public class IfElse {
    /**
     * @param args
     * Clase demostrariva de if else
     */
    public static void main(String[] args) {
        
        boolean check = 5<10;

        if (check) {
            System.out.println(true);
        } else {
            System.out.println(false);
        }

        
    }
}
